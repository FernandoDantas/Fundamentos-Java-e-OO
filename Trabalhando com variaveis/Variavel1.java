public class Variavel1 {
	
	public static void main(String[] args){
		int quantidade;//Declarada a variavel e o tipo como inteiro
		quantidade = 10;//Variavel recebe um valor atribuindo o valor 10
		
		System.out.println(quantidade);
		
		quantidade = 15;//Troca o valor da variavel para 15
		
		System.out.println(quantidade);
		
	}
	
}

